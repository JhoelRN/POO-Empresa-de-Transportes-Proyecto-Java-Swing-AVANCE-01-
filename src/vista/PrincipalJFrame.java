    private void tab5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab5MouseClicked
        // CLICK EN SALIR
        // Cambiamos interfaz para confirmar salida
        int eleccion = JOptionPane.showConfirmDialog(this,"Desea cerrar sesión?", "Confirme por favor", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        //segun la eleccion
        
        if (eleccion == JOptionPane.YES_OPTION) {// la respuesta es SI?
            //System.exit(0);//sale de la aplicación
            this.dispose();
            LoginJFrame loginNew = new LoginJFrame();
            loginNew.setVisible(true);
            
        }
        else if (eleccion == JOptionPane.NO_OPTION) {// la respuesta es NO?
            // no se hace nada
        }
        else if(eleccion == JOptionPane.CLOSED_OPTION){// la respuesta es cerrar? //cerrar caja de dialogo
            // no se hace nada
        }
    }//GEN-LAST:event_tab5MouseClicked

